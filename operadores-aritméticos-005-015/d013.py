# Escreva um programa que leia o salário de um funcionário e mostre o seu novo salário com 15% de aumento

# Recebendo o salário do funcionário

salario = float(input('Digite o seu salário:'))

# Reajuste salarial

reajuste_salario = salario * 1.15
print('O seu novo salário é:', reajuste_salario)