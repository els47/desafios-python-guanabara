# Crie um programa que leia um número inteiro e mostre na sua tela se ele é par ou ímpar

# Recebendo um valor inteiro

valor = int(input('Digite um valor inteiro:'))

# Verificando se é par ou ímpar

if valor %2 == 0:
    print('Este é um número par!')
else:
    print('Este é um número ímpar!')
